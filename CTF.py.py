# Meminta pengguna untuk memasukkan input nilai
celcius = float(input("Masukkan suhu dalam derajat celcius = "))
# Menghitung suhu Fahreheit 
fahrenheit = (celcius * 9/5) + 32
# Untuk mencetak output
print(f"{celcius} derajat Celcius = {fahrenheit:.1f} derajat Fahrenheit")
# 1.f untuk menformat suhu yang sudah dikonversi hanya satu angka desimal